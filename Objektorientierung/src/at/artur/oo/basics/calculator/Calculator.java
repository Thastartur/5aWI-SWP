package at.artur.oo.basics.calculator;

public class Calculator {

    public double addNumbers(double number1, double number2){
       double result =  number1 + number2;
       return result;
    }

    public double subNumbers(double number1, double number2){
        double result =  number1 - number2;
        return result;
    }

    public double multNumbers(double number1, double number2){
        double result =  number1 * number2;
        return result;
    }

    public double divNumbers(double number1, double number2){
        double result =  number1 / number2;
        return result;
    }
}
